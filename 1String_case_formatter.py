text="PyThOn"
lower_text=text.lower()   #str.lower() – Converts all characters to lowercase.

upper_text=text.upper()  #str.upper() – Converts all characters to uppercase.
swap_text=text.swapcase()  #str.swapcase() – Converts uppercase characters to lowercase and vice versa.

print(upper_text)
print(lower_text)
print(swap_text)